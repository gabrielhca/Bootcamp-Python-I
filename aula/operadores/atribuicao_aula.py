# '=' atribui um valor a uma variavel
resultado = 2
print(resultado)

# atribuicao composta
resultado += 3
print(resultado)
resultado -= 2
print(resultado)
resultado *= 2
print(resultado)
resultado /= 5
print(resultado)
resultado %= 4
print(resultado)

# uma variavel pode receber um novo valor de outro tipo
resultado = 'texto'
print(resultado)